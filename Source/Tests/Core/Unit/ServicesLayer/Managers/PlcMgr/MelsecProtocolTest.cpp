//=============================================================================
void MelsecProtocolTest::ConstructorTest(void)
{
    // make sure m_melsecComDev is -1
    CPPUNIT_ASSERT(imp->m_melsecComDev == -1);
    // make sure m_stationNumber is 0
    CPPUNIT_ASSERT(imp->m_stationNumber == 0);
    // make sure m_pcNumber is 0xFF
    CPPUNIT_ASSERT(imp->m_pcNumber == 0xFF);
}

//=============================================================================
void MelsecProtocolTest::OpenTest(void)
{
    int retval=0;

    retval = imp->Open(11);

    // make sure the operation returned EOK
    CPPUNIT_ASSERT(retval == EOK);
    // make sure the comm port is set correctly
    CPPUNIT_ASSERT(imp->m_melsecComDev == 11);
    // make sure the lock was released
    CPPUNIT_ASSERT(imp->m_melsecLock.TryAcquire() == EOK);
}

//=============================================================================
void MelsecProtocolTest::CloseTest(void)
{
    int retval=0;

    retval = imp->Close();

    // make sure the opereration returned EOK
    CPPUNIT_ASSERT(retval == EOK);
    // make sure the comm port was reset
    CPPUNIT_ASSERT(imp->m_melsecComDev == -1);
    // make sure the lock was released
    CPPUNIT_ASSERT(imp->m_melsecLock.TryAcquire() == EOK);
}

//=============================================================================
void MelsecProtocolTest::Read_MutexFailure(void)
{
    int retval = 0;
    uint32_t buff[27];

    // grab the mutex, so the read will fail
    imp->m_melsecLock.Acquire();

    retval = imp->Read(buff,27);

    // release the mutex
    imp->m_melsecLock.Release();

    CPPUNIT_ASSERT_EQUAL(0,retval);
}

//=============================================================================
void MelsecProtocolTest::Write_MutexFailure(void)
{
    int retval = 1;
    const uint32_t plcOut[2] = {0,1};

    // grab the mutex, so the read will fail
    imp->m_melsecLock.Acquire();

    retval = imp->Write(plcOut,2);

    // release the mutex
    imp->m_melsecLock.Release();

    CPPUNIT_ASSERT_EQUAL(0,retval);
}

//=============================================================================
void MelsecProtocolTest::HexToLongTest(void)
{
    long retVal = 0;

    // the valid values are (0-9), (A-F), and space
    uint8_t buff[3] = {'1','F',' '};

    retVal = imp->HexToLong(buff,3l);
    CPPUNIT_ASSERT_EQUAL(496l,retVal);
}

